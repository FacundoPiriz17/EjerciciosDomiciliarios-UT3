public class ParejaSueroFarmaco {
    private int idSuero;
    private int idFarmaco;

    public ParejaSueroFarmaco(int idSuero, int idFarmaco) {
        this.idSuero = idSuero;
        this.idFarmaco = idFarmaco;
    }

    public int getIdSuero() {
        return idSuero;
    }

    public int getIdFarmaco() {
        return idFarmaco;
    }
}